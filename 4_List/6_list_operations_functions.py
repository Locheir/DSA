### List Operations / Functions

# Concat two lists using '+' operator : 
a = [1,2,3]
b = [4,5,6]
c = a + b
print(c)

# Repeat a list or an element for 'n' times using '*' operator :
d = [0,1]
d = d * 4
print(d)

# len() function : gives the length of the list.
e = [23,32,17,85,47,2,98]
print(len(e))

# max() function : find maximum element in the list.
f = [23,32,17,85,47,2,98]
print(max(f))

# min() function : find minimum element in the list.
g = [23,32,17,85,47,2,98]
print(min(g))

# sum() function : find the sum of all elements of the list.
h = [23,32,17,85,47,2,98]
print(sum(h))