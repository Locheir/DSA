## Array vs List :

# Similarities : 
# 
# * Both are mutable in nature.
# * Both can be indexed and iterated through.
# * They can both be sliced.

# Possible : using Numpy Array 
import numpy as np
myArray = np.array([1,2,3,4,5])

print(myArray/2)

# Not Possible : using Python List
list = [11,22,33,44]
print(list/2)