## Python List : 
#
# * A List is a data structure that holds an ordered collection of items.
# * A list can contain any type of variable, including other lists.
# * A list can contain variables of different datatypes.
# * Lists are mutable in nature.

# Example :
l1 = [1,2,3,4]
print(l1)

l2 = [1,"Mohit",True,4+2j,1.4]
print(l2)