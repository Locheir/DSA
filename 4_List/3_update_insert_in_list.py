### Updating / Inserting in List : 

## Updating in a List : 
myList = [1,2,3,4,5,6,7]
print(myList)

myList[2] = 33
print(myList)

## Inserting in a List : 

# Inserting single element at any positon.
myList.insert(4,10)
print(myList)

# Append single element at the last position of list.
myList.append(200)
print(myList)

# Extending another list to the end of the list.
myList.extend([300,400])
print(myList)