# Python Dictionary : 
# A dictionary is a collection which is unordered, 
# changeble and indexed.

# Example of Dictionary : 
# Creating Dictionary Normally : 
# Student Data : 
Students = {
    "name" : "Mohit",
    "age" : 21,
    "class" : "F.Y",
    "roll" : 17
}
print(Students)

# We can create an empty dictionary using 
# 'dict()' constructor or using '{}' :
my_dict = dict()
print(my_dict)

# Creating a dictionary using 'dict()' :
eng_spa = dict(one='uno', two='dos', three='tres')
print(eng_spa)