## Arrays in Python 
#
# There are no build-in datatypes for arrays in Python ,
# Hence we have to import libraries like numpy to use array.
#
#    * Array can store data of specified types.
#    * Elements of an array are located in a 
#      contiguous memory location.
#    * Each elements of an array has a unique index.


## Types of Arrays : 
#
#    * One-Dimensional Array   : 1D
#    * Multi-Dimensional Array : 2D, 3D, 4D


## 1-D Array : 
#
#   Array     => |  02   |  07   |  11   |  13   |  89   |  76   |  88   |  52   |  39   |
